    // This class solves the labyrinth shortest pathfinding problem with the
    // addition of the single wall removal. The pathfinding algorithm is based on
    // Dijkstra`s pathfinding algorithm
    // https://en.wikipedia.org/wiki/Dijkstra%27s_algorithm, others could also be
    // considered, such as A*
    public class Solution {

        // List containing all valid nodes
        static List<Node> nodes;
        // List containing our path
        static List<Node> path;

        // Running the pathfinding algorithm once, then trying to find a better solution
        // with wall removals
        public static int solution(int[][] map) {

            int steps = findPath(map);

            for (int i = 0; i < map.length; i++) {
                for (int j = 0; j < map[i].length; j++) {
                    if (map[i][j] == 1) {
                        int[][] mapcopy = copymap(map);
                        mapcopy[i][j] = 0;
                        int newSteps = findPath(mapcopy);
                        if (newSteps < steps) {
                            steps = newSteps;
                        }
                    }
                }
            }

            return steps;
        }

        // Pathfinding algorithm based on Dijkstra`s algorithm
        static int findPath(int[][] map) {

            // Reset nodes and path
            nodes = new ArrayList<Node>();
            path = new ArrayList<Node>();

            // Generate valid nodes from the map
            for (int i = 0; i < map.length; i++) {
                for (int j = 0; j < map[i].length; j++) {
                    if (map[i][j] == 0) {
                        nodes.add(new Node(i, j));
                    }
                }
            }
            // Generate neighbors and connections for nodes
            for (int i = 0; i < nodes.size(); i++) {
                for (int j = 0; j < nodes.size(); j++) {
                    if (nodes.get(i).distance(nodes.get(j)) == 1) {
                        nodes.get(i).connections.add(nodes.get(j));
                    }
                }
            }

            // Unitialize the unvisited list
            List<Node> unvisited = new ArrayList<Node>();

            // Set up the unvisited list
            for (int i = 0; i < nodes.size(); i++) {
                unvisited.add(nodes.get(i));
                // The distance by default is infinite
                unvisited.get(i).distance = Double.POSITIVE_INFINITY;
                // The previous best node is null in the beginning
                unvisited.get(i).prev = null;
            }

            // Initialize the starting point
            Node start = nodes.get(0);
            // Distance from start is 0
            start.distance = 0;
            // Initialize target node
            Node target = nodes.get(nodes.size() - 1);

            // Process the unvisited nodes
            while (!unvisited.isEmpty()) {
                // Find the closest node as our next node
                Node closest = unvisited.get(0);
                for (int i = 0; i < unvisited.size(); i++) {
                    if (unvisited.get(i).distance < closest.distance) {
                        closest = unvisited.get(i);
                    }
                }
                // Remove the found node from the unvisited list
                unvisited.remove(closest);

                // If we reached the target then calculate path length and finish processing
                if (closest.equals(target)) {

                    path.add(closest);

                    while (closest.prev != null) {
                        path.add(closest.prev);
                        closest = closest.prev;
                    }
                    break;
                }
                // If the node was not the target node then process its connections that are
                // still in the unprocessed list
                for (int i = 0; i < closest.connections.size(); i++) {
                    if (unvisited.contains(closest.connections.get(i))) {
                        // The new distance is based on the distance so far and the Euclidean distance
                        // between the two connected nodes
                        double newdist = closest.distance + closest.distance(closest.connections.get(i));
                        // If the new distance is smaller than the saved distance then update it
                        if (newdist < closest.connections.get(i).distance) {
                            closest.connections.get(i).distance = newdist;
                            closest.connections.get(i).prev = closest;
                        }
                    }
                }

            }
            // If we have a path length of one then return infinite, it is not possible
            // having to only take a single step to solve the labyrinth, otherwise reuturn
            // the path length
            if (path.size() == 1) {
                return Integer.MAX_VALUE;
            } else {
                return path.size();
            }
        }

        // Create deepcopy of the map
        static int[][] copymap(int[][] map) {
            int[][] mapcopy = new int[map.length][map[0].length];

            for (int c = 0; c < map.length; c++) {
                for (int k = 0; k < map[0].length; k++) {
                    mapcopy[c][k] = map[c][k];
                }
            }
            return mapcopy;
        }

        // Class representing the nodes in our labyrinth
        static class Node {

            public int x, y;
            public List<Node> connections = new ArrayList<Node>();
            public double distance;
            public Node prev;

            public Node(int x, int y) {
                this.x = x;
                this.y = y;
            }

            // Simple Euclidean distance
            public double distance(Node other) {
                int x = this.x - other.x, y = this.y - other.y;
                return Math.sqrt(x * x + y * y);
            }
        }
    }
